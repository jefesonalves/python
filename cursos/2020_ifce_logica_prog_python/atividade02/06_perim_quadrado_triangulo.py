print("## Cálculo do perímetro do quadrado ##")
lado01 = float(input("Digite o valor do lado do quadrado em cm: "))
perimetro_quadrado = 4 * lado01
print(f"O perímetro do quadrado é igual: {perimetro_quadrado} cm")

print("## Cálculo do perímetro do triângulo ##")
lado01 = float(input("Digite o primeiro lado do triângulo em cm: "))
lado02 = float(input("Digite o segundo lado do triângulo em cm: "))
lado03 = float(input("Digite o terceiro lado do triângulo em cm: "))

perimetro_triangulo = lado01 + lado02 + lado03
print(f"O perímetro do triângulo é igual: {perimetro_triangulo} cm")