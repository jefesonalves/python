print("## Cálculo da área do quadrado ##")
lado = float(input("Digite o tamanho do lado do quadrado em cm: "))
area_quadrado = lado * lado
print(f"A área do quadrado é igual: {area_quadrado} cm²")

print("## Cálculo da área do triângulo ##")
base = float(input("Digite o tamanho da base do triângulo em cm: "))
altura = float(input("Digite o tamanho da altura do triângulo em cm: "))
area_triangulo = base * altura / 2
print(f"A área do triângulo é igual: {area_triangulo} cm²")