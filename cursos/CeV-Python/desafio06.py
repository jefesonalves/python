# Desafio 05 do Curso em Vídeo

a = int(input('Digite um valor: '))

dobro = int(a ** 2)
triplo = int(a ** 3)
raiz = int(a ** (1/2))

print('O dobro de {:.2f} é igual a {:.2f}'.format(a, dobro))
print('O triplo de {:.2f} é igual a {:.2f}'.format(a, triplo))
print('A raiz quadrada de {:.2f} é igual a {:.2f}'.format(a, raiz))
